    private void convertBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_convertBtnActionPerformed
        try {
            // TODO add your handling code here:

//            fromCode=fromCurrency.getSelectedItem().toString();
            amount = Double.parseDouble(amountTxt.getText().toString());

            String GET_URL = "https://v6.exchangerate-api.com/v6/70214c72bf036fd5cc2e6c00/pair/" + fromCode + "/" + toCode;

            URL url = new URL(GET_URL);
            HttpURLConnection httpURLConnection = (HttpURLConnection) url.openConnection();
            httpURLConnection.setRequestMethod("GET");
            int responseCode = httpURLConnection.getResponseCode();

            if (responseCode == HttpURLConnection.HTTP_OK) {
                BufferedReader in = new BufferedReader(new InputStreamReader(httpURLConnection.getInputStream()));
                String inputLine;
                StringBuffer response = new StringBuffer();

                while ((inputLine = in.readLine()) != null) {
                    response.append(inputLine);
                }
                in.close();

                JSONObject obj = new JSONObject(response.toString());
                System.out.println(obj);

                double exchangeRate = obj.getDouble("conversion_rate");
                System.out.println(exchangeRate);

                double convertedAmount = amount * exchangeRate;
                String message=amount+" "+fromCode + " = "+ convertedAmount+" "+toCode;
                System.out.println(message);
                
                JOptionPane.showMessageDialog(this,message, "Currency Calculator", JOptionPane.INFORMATION_MESSAGE);

            } else {
                System.out.println("GET request failed");
                JOptionPane.showMessageDialog(this,"GET request failed", "Currency Calculator", JOptionPane.ERROR_MESSAGE);

            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Some error occured", "Currency Calculator", JOptionPane.ERROR_MESSAGE);

        }


    }//GEN-LAST:event_convertBtnActionPerformed

