    private void currencyData() throws Exception {
        String GET_DATA_URL = "https://v6.exchangerate-api.com/v6/70214c72bf036fd5cc2e6c00/codes";

        URL url = new URL(GET_DATA_URL);
        HttpURLConnection httpURLConnection = (HttpURLConnection) url.openConnection();
        httpURLConnection.setRequestMethod("GET");
        int responseCode = httpURLConnection.getResponseCode();

        if (responseCode == HttpURLConnection.HTTP_OK) {
            BufferedReader in = new BufferedReader(new InputStreamReader(httpURLConnection.getInputStream()));
            String inputLine;
            StringBuffer response = new StringBuffer();

            while ((inputLine = in.readLine()) != null) {
                response.append(inputLine);
            }
            in.close();

            JSONObject obj = new JSONObject(response.toString());
            System.out.println(obj);

            JSONArray rows = obj.getJSONArray("supported_codes");

            int count = rows.length();
            for (int i = 0; i < count; i++) {
                jsonArr = rows.getJSONArray(i);
                System.out.println("jsonObject " + i + ": " + jsonArr);
                val = jsonArr.toString();
                fromCurrency.addItem(val);
                toCurrency.addItem(val);

            }

        } else {
            System.out.println("GET request failed");
            JOptionPane.showMessageDialog(this,"GET request failed", "Currency Calculator", JOptionPane.ERROR_MESSAGE);

        }

    }

